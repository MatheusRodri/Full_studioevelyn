create database studio;
USE STUDIO;


CREATE TABLE AGENDAMENTOS (
    ID INT AUTO_INCREMENT PRIMARY KEY,
    CPF  VARCHAR(14) NOT NULL,
    NOME VARCHAR(255) NOT NULL,
    DATA DATE NOT NULL,
    HORA TIME NOT NULL,
    PROCEDIMENTO VARCHAR(255) NOT NULL,
    TP_PAGAMENTO VARCHAR (255) NOT NULL 
);



